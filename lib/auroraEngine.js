  // Returns the activity multiplier: idle=0.15, peak=1.3, ease-in curve
  _getActivityMultiplier() {
    const a = this.activityLevel;
    const eased = Math.pow(a, 1.5); // ease-in: gentle at low, accelerates at high
    return 0.15 + 1.15 * eased;
  }

  // Returns a speed multiplier: idle=0.4, peak=1.6
  _getActivitySpeedMultiplier() {
    const a = this.activityLevel;
    return 0.4 + 1.2 * a;
  }

  _lerp(a, b, t) { return a + (b - a) * t; }

  // Compute animation offsets for a single column based on active effects
  _computeColumnEffects(i, x1, y1, colCount, W) {
    const eff = this.effects;
    const t = this.time;
    const actMul = this._getActivityMultiplier();
    const spdMul = this._getActivitySpeedMultiplier();
    let swayX = 0, driftY = 0, brightMul = 1, hueOff = 0, gradShift = 0;

    // 1. Column Sway — noise-driven lateral drift (big, visible curtain dance)
    if (eff.sway.enabled) {
      const amt = eff.sway.intensity * 120 * actMul;
      // Two noise layers: slow drift + faster flutter
      const slowDrift = this.noise.noise2D(i * 0.25, t * 0.008 * spdMul) * amt * 0.7;
      const fastFlutter = this.noise.noise2D(i * 0.6, t * 0.025 * spdMul) * amt * 0.3;
      swayX = slowDrift + fastFlutter;
    }

    // 2. Gradient Wave — undulating color bands flowing through columns
    if (eff.gradientWave.enabled) {
      gradShift = Math.sin(i * 0.5 - t * 0.03 * spdMul) * eff.gradientWave.intensity * 0.25 * actMul;
    }

    // 3. Brightness Ripple — rolling wave of light across the curtain
    if (eff.brightnessRipple.enabled) {
      const normX = x1 / W;
      const wave = (Math.sin(normX * 6 - t * 0.035 * spdMul) + 1) * 0.5;
      // At full intensity: columns go from 10% to 100% brightness
      const range = eff.brightnessRipple.intensity * actMul;
      brightMul *= (1 - range * 0.9) + wave * range * 0.9;
    }

    // 4. Twinkle Pulse — random columns flash bright then fade
    if (eff.twinklePulse.enabled) {
      const phase = this.noise.noise2D(i * 2.3, 777) * 200;
      const speed = 0.8 + this.noise.noise2D(i * 1.7, 333) * 0.6;
      const pulse = (Math.sin((t + phase) * 0.03 * speed * spdMul) + 1) * 0.5;
      // At full intensity: columns flash between 5% and 100%
      const range = eff.twinklePulse.intensity * actMul;
      brightMul *= (1 - range * 0.95) + pulse * range * 0.95;
    }

    // 5. Color Temperature Breathing — purple ↔ teal shift
    if (eff.colorBreathing.enabled) {
      hueOff = Math.sin(t * 0.012 * spdMul) * eff.colorBreathing.intensity * 50 * actMul;
    }

    // 6. Soft Vertical Drift — gradient slides up and down
    if (eff.verticalDrift.enabled) {
      driftY = Math.sin(t * 0.018 * spdMul) * eff.verticalDrift.intensity * 40 * actMul;
    }

    return { swayX, driftY, brightMul, hueOff, gradShift };
  }

  _lerpConfig() {
    for (const k in this.targetConfig) {
      this.currentConfig[k] = this._lerp(this.currentConfig[k], this.targetConfig[k], 0.006);
    }
    // Smooth lerp for activity level (very smooth, ~2-3 seconds to settle)
    this.activityLevel = this._lerp(this.activityLevel, this.targetActivityLevel, 0.008);
  }

  /* ── Render: single curtain for a guide curve ─── */

  _renderCurtain(bc, curve, ci, numCurves, W, H, params) {
    const { time, hS, sat, pulse, cfg, purpleHue, tealHue } = params;
    const pts = curve.points;
    if (pts.length < 3) return;

    const curveT = ci / numCurves;
    const yNorm = Math.max(0, Math.min(1, curve.avgY));

    const n1 = (this.noise.noise2D(curveT * 10, time * 0.25) + 1) * 0.5;
    const n2 = (this.noise2.noise2D(curveT * 6, time * 0.18 + 5) + 1) * 0.5;
    const curveBright = Math.pow(n1 * 0.6 + n2 * 0.4, 0.8);
    const baseAlpha = curveBright * cfg.brightness * this.intensity * pulse;
    if (baseAlpha < 0.06) return;

    const baseDropH = H * (0.06 + 0.06 * yNorm);
    const baseFadeH = H * (0.14 + 0.08 * yNorm);

    bc.beginPath();
    for (let i = 0; i < pts.length; i++) {
      const segT = i / pts.length;
      const fadeN = (this.noise2.noise2D(curveT * 3 + segT * 3, time * 0.07) + 1) * 0.5;
      const fadeUp = baseFadeH * (0.7 + fadeN * 0.3);
      const px = pts[i].x * W;
      const py = pts[i].y * H - fadeUp;
      if (i === 0) bc.moveTo(px, py);
      else bc.lineTo(px, py);
    }
    for (let i = pts.length - 1; i >= 0; i--) {
      const segT = i / pts.length;
      const segN = (this.noise.noise2D(curveT * 4 + segT * 3, time * 0.08) + 1) * 0.5;
      const drop = baseDropH * (0.75 + segN * 0.25);
      bc.lineTo(pts[i].x * W, pts[i].y * H + drop);
    }
    bc.closePath();

    const minY = Math.min(...pts.map(p => p.y));
    const maxDrop = baseDropH * 1.2;
    const gradTop = (minY - baseFadeH / H) * H;
    const gradBot = (Math.max(...pts.map(p => p.y)) + maxDrop / H) * H;

    const grad = bc.createLinearGradient(W * 0.5, gradTop, W * 0.5, gradBot);
    const a = Math.min(1, baseAlpha * 2.0);
    const crownStrength = 0.6 + 0.4 * yNorm;
    grad.addColorStop(0,    'rgba(0,0,0,0)');
    grad.addColorStop(0.40, 'rgba(0,0,0,0)');
    grad.addColorStop(0.48, this._hslToRgba(purpleHue, 0.50 * sat, 0.52, a * 0.06 * crownStrength));
    grad.addColorStop(0.54, this._hslToRgba(purpleHue, 0.55 * sat, 0.50, a * 0.22 * crownStrength));
    grad.addColorStop(0.60, this._hslToRgba(purpleHue - 8, 0.56 * sat, 0.48, a * 0.44));
    grad.addColorStop(0.68, this._hslToRgba(purpleHue - 20, 0.58 * sat, 0.46, a * 0.62));
    grad.addColorStop(0.76, this._hslToRgba(220 + hS, 0.60 * sat, 0.45, a * 0.65));
    grad.addColorStop(0.84, this._hslToRgba(195 + hS, 0.65 * sat, 0.47, a * 0.55));
    grad.addColorStop(0.92, this._hslToRgba(tealHue, 0.75 * sat, 0.52, a * 0.42));
    grad.addColorStop(1,    this._hslToRgba(tealHue, 0.80 * sat, 0.58, a * 0.50));

    bc.fillStyle = grad;
    bc.fill();

    const bulbStep = Math.max(3, Math.floor(pts.length / 25));
    for (let i = 0; i < pts.length; i += bulbStep) {
      const segT = i / pts.length;
      const segN = (this.noise.noise2D(curveT * 4 + segT * 3, time * 0.08) + 1) * 0.5;
      const drop = baseDropH * (0.75 + segN * 0.25);
      const bx = pts[i].x * W;
      const by = pts[i].y * H + drop;
      const br = W * 0.008 * (0.5 + segN);
      const ba = Math.min(1, baseAlpha * 0.35 * (0.4 + segN));

      const bulbG = bc.createRadialGradient(bx, by, 0, bx, by, br);
      bulbG.addColorStop(0, this._hslToRgba(tealHue, 0.82 * sat, 0.65, ba));
      bulbG.addColorStop(0.5, this._hslToRgba(tealHue, 0.70 * sat, 0.55, ba * 0.4));
      bulbG.addColorStop(1, 'rgba(0,0,0,0)');
      bc.fillStyle = bulbG;
      bc.beginPath();
      bc.arc(bx, by, br, 0, Math.PI * 2);
      bc.fill();
    }
  }

